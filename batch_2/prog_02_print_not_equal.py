# Prog02: Create a program that ask user to input 2 numbers. Print "Not Equal" when the numbers are not the same.

# ask user two inputs
if int(input("Input your first number: ")) != int(input("Input your second number: ")):
# print not equal if inputs are not equal
    print ("Your numbers are not equal.")
# print equal if inputs are equal
else: 
    print ("Your numbers are equal.")